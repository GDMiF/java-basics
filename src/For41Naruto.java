public class For41Naruto {

    String name;
    String gmail;
    int age;

    For41Naruto(String name){
        this.name = name;
        this.gmail = "Unknown";
        this.age = 0;
    }

    For41Naruto(String name, String gmail){
        this.name = name;
        this.gmail = gmail;
        this.age = 0;
    }

    For41Naruto(String name, String gmail, int age){
        this.name = name;
        this.gmail = gmail;
        this.age = age;
    }

    For41Naruto(){
        this.name = "Invalid";
        this.gmail = "Unknown";
        this.age = 0;
    }

}
